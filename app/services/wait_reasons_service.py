    @classmethod
    def get_reason_text(cls, reason_id: str) -> str:
        """Получает текст причины по ID"""
        return cls.REASON_MAP.get(str(reason_id), f"Причина #{reason_id}")
    
    @classmethod
    def get_action_description(cls, reason_id: str) -> str:
        """Получает детальное описание действия по ID"""
        reason_text = cls.get_reason_text(reason_id)
        return cls.ACTION_DESCRIPTIONS.get(reason_text, f"Выполнить действие #{reason_id}")
    
    @classmethod
    def group_by_risks(cls, reason_ids: List[str]) -> Dict[str, List[str]]:
        """
        Группирует причины по рискам.
        
        Args:
            reason_ids: Список ID причин
        
        Returns:
            Словарь {риск: [причины]}
        """
        result = {}
        
        for reason_id in reason_ids:
            reason_text = cls.get_reason_text(reason_id)
            
            # Ищем риск для этой причины
            for risk, reasons in cls.RISK_GROUPS.items():
                if reason_text in reasons:
                    if risk not in result:
                        result[risk] = []
                    if reason_text not in result[risk]:
                        result[risk].append(reason_text)
                    break
        
        return result
    
    @classmethod
    def format_action_items(cls, reason_ids: List[str]) -> List[Tuple[str, str]]:
        """
        Формирует список action items (причина → риск).
        
        Args:
            reason_ids: Список ID причин
        
        Returns:
            Список кортежей (причина, риск)
        """
        result = []
        
        for reason_id in reason_ids:
            reason_text = cls.get_reason_text(reason_id)
            action_desc = cls.get_action_description(reason_id)
            
            # Находим риск
            risk_text = "Риск не определён"
            for risk, reasons in cls.RISK_GROUPS.items():
                if reason_text in reasons:
                    risk_text = cls.RISK_DESCRIPTIONS.get(risk, risk_text)
                    break
            
            result.append((action_desc, risk_text))
        
        return result
    
    @classmethod
    def get_general_risk(cls, reason_ids: List[str], product_codes: List[str]) -> str:
        """
        Формирует общий риск на основе причин и продуктов.
        
        Args:
            reason_ids: Список ID причин
            product_codes: Список кодов продуктов
        
        Returns:
            Текст общего риска
        """
